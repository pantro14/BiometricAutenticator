package carvajal.autenticador.android.bl.exception;

public class ProvinciasBLException extends Exception {
	private static final long serialVersionUID = 1L;

	public ProvinciasBLException() {
		super();
	}

	public ProvinciasBLException(final String message) {
		super(message);
	}

	public ProvinciasBLException(final String message, final Throwable cause) {
		super(message, cause);
	}

	public ProvinciasBLException(final Throwable cause) {
		super(cause);
	}
}
